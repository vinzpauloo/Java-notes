public class ArithmeticExpressions {

    public static void arithmeticexpressions (String[] args) {

        // expression = operands & operators
        // operands = values, variables, numbers, quantity
        // operators = + - * / %

        /*
        Data Types:

        boolean = true or false
        int = -2 billion to 2 billion
        double = fractional number up to 15 digits
        char = single character or letter or ASCII value
        String = sequence of characters

        Not frequently used:

        byte = -128 to 127
        short = -32,768 to 32,767
        long = -9 quintillion to 9 quintillion
        float = fractional number up to 6-7 digits
         */

        double friends = 10;

        friends = (double) friends / 3;

        System.out.println(friends);
    }
}
